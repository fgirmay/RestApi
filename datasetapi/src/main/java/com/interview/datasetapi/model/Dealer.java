package com.interview.datasetapi.model;

public class Dealer {

    private int dealerId;
    private String name;

    public Dealer() {
    }

    public int getDealerId() {
        return dealerId;
    }

    public void setDealerId(int dealerId) {
        this.dealerId = dealerId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
